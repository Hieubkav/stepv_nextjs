const DEFAULT_ICON = Sparkles;

export function getLucideIcon(key?: string): LucideIcon {
  if (!key) {
    return DEFAULT_ICON;
  }

  return ICON_MAP[key as IconKey] ?? DEFAULT_ICON;
}
